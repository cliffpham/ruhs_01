void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int remainer;

	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb < 10)
	{
		ft_putchar(nb + 48);
	}
	else
	{
		ft_putnbr(nb / 10);
		remainer = nb % 10;
		ft_putchar(remainer + 48);
	}
}
